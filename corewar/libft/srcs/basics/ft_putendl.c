#include <unistd.h>

void	ft_putendl(char const *s)
{
	int		i;

	if (s && !(i = 0))
	{
		while (s[i])
			i++;
		if (write(1, s, i) == -1)
			return ;
		if (write(1, "\n", 1) == -1)
			return ;
	}
}

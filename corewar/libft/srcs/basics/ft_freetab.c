#include <stdlib.h>

void	ft_freetab(char ***tab)
{
	int	i;

	if (*tab)
	{
		i = 0;
		while ((*tab)[i])
		{
			free((*tab)[i]);
			(*tab)[i++] = NULL;
		}
		free(*tab);
		*tab = NULL;
	}
}

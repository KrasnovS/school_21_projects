#include "libft.h"

void	ft_strclr(char *s)
{
	size_t	i;

	if (s == NULL)
		return ;
	i = ft_strlen(s);
	i++;
	while (i > 1)
	{
		s[i - 2] = '\0';
		i--;
	}
}

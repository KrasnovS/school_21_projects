#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	size_t	i;
	char	*a;

	i = 0;
	a = (char*)s;
	while (s[i] != '\0')
	{
		if (s[i] == c)
			return (a + i);
		i++;
	}
	if (c == 0)
		return (a + i);
	return (NULL);
}

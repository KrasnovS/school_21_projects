#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	int		i;
	size_t	q;

	i = 0;
	while (s1[i] != '\0')
		i++;
	q = 0;
	while (s2[q] != '\0' && q < n)
	{
		s1[i] = s2[q];
		i++;
		q++;
	}
	s1[i] = '\0';
	return (s1);
}

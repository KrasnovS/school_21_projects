#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	unsigned int	i;
	unsigned int	q;

	i = 0;
	while (s1[i] != '\0')
		++i;
	q = 0;
	while (s2[q] != '\0')
	{
		s1[i] = s2[q];
		i++;
		++q;
	}
	s1[i] = '\0';
	return (s1);
}
